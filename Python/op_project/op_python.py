a = 100
b = 15

c = a + b # Addition Operator
print("addition: ", c)

c = a - b #Subtraction Operator
print("subtraction: ", c)

c = a * b # Multiplication Operator
print("Multiplication: ", c)

c = a / b # Division Operator
print("Division: ", c)

c = a % b # Modulo/Remainder Operator
print("Modulo/Remainder: ", c)

c = a // b # Floor Division Operator
print("Floor Division: ", c)

c = a ** b # Exp Operator
print("Exp: ", c)

c = -a # Minus Operator
print("Minus: ", c)
